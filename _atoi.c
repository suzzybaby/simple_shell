/**
 * _isalpha - checks for alphabetic char
 * @c: The char to input
 * Return: 1 if c is alpha, 0 otherwise
 */

int _isalpha(int c)
{
	if ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z'))
		return (1);
	else
		return (0);
}

